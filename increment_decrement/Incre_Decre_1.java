package com.increment_decrement;

public class Incre_Decre_1 {

	public static void main(String[] args) {
//increment
		int a = 10;
		a++;
		System.out.println(a);// 11
		a = a + 1;
		System.out.println(a);// 12
//Decrement
		int d=200;
		d--;
		System.out.println(d);//199
		a=d--;
		System.out.println(a);//199
	}

}
