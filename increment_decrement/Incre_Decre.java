package com.increment_decrement;

public class Incre_Decre {

	public static void main(String[] args) {
		int a = 100;

//increment
		a++;
		System.out.println(a);// 101
		a = a + 1;
		System.out.println(a);// 102
		a++;
		System.out.println(a);// 103
		
//decrement 
		a--;
		System.out.println(a);//102
		int m=200;
		System.out.println(m);//200
		a=m-1;
		System.out.println(a);//199
			
	}

}
